#define SQL_ATTR_LOBLOCATOR		SQL_DRIVER_CONNECT_ATTR_BASE + 11

/*attribute to get a verbose version string*/
#define SQL_DRIVER_VERBOSE_VER	SQL_DRIVER_CONNECT_ATTR_BASE + 12

#define SQL_LOBLOCATOR_ON				1
#define SQL_LOBLOCATOR_OFF				0

#define SQL_ATTR_LOBLOCATOR_FETCHSIZE	SQL_DRIVER_STATEMENT_ATTR_BASE + 10

