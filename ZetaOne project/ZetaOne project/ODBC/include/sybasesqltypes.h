#endif

/*	LOCATOR DEFINITIONS */
/*locator SQL TYPE*/
#define SQL_TEXT_LOCATOR		SQL_DRIVER_SQL_TYPE_BASE +10
#define SQL_UNITEXT_LOCATOR		SQL_DRIVER_SQL_TYPE_BASE +11
#define SQL_IMAGE_LOCATOR		SQL_DRIVER_SQL_TYPE_BASE +13

#ifndef SQL_DRIVER_STATEMENT_ATTR_BASE
#define SQL_DRIVER_STATEMENT_ATTR_BASE 0x00004000
#endif

#ifndef SQL_DRIVER_C_DATATYPES_BASE
#define SQL_DRIVER_C_DATATYPES_BASE 0x4000
#endif

#ifndef SQL_DRIVER_C_DATATYPES_END
#define SQL_DRIVER_C_DATATYPES_END 0x7FFF
#endif



/*locator C TYPE */
#define SQL_C_TEXT_LOCATOR		SQL_DRIVER_C_DATATYPES_BASE + 1
#define SQL_C_UNITEXT_LOCATOR	SQL_DRIVER_C_DATATYPES_BASE + 2
#define SQL_C_IMAGE_LOCATOR		SQL_DRIVER_C_DATATYPES_BASE + 3

/*value for the Locator size */
#define SQL_LOCATOR_SIZE		24

