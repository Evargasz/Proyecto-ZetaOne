#define SQL_BATCH_DISABLED		0
#define SQL_BATCH_ENABLED		1
#define SQL_BATCH_LAST_NO_DATA	(-1)
#define SQL_BATCH_LAST_DATA		(-2)
#define SQL_BATCH_CANCEL		(-3)

/* SQLRETURN for SQLExecute and SQLExecDirect while in a batch */
#define SQL_BATCH_EXECUTING		200





